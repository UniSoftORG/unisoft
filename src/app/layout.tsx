import { VerticalNavigation } from "@/predefined/components/Navigations";
import { generateComponentForRendering } from "@/renderer/helpers/generateComponentForRendering";
import type { Metadata } from "next";
import dynamic from "next/dynamic";
import { Suspense } from "react";
import "./globals.css";

const LayoutComponent = dynamic(() => import("./layoutComponent"));
export const metadata: Metadata = {
  title: "UniSoft",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const layout = await generateComponentForRendering(VerticalNavigation);
  return (
    <html lang='en'>
      <body>
        <Suspense>
          <LayoutComponent data={layout} />
        </Suspense>
        <Suspense>{children}</Suspense>
      </body>
    </html>
  );
}
